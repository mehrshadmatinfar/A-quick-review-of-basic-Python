# Sequence
a = 'hello world!'
print(a[1])  # first array is array number "0"
print(a[1:5])
print(a[:5])

print('Length = ' + str(len(a)))

# array & List
List = [[1, 2, 3],
        [5, 8, 3],
        [9, 6, 4],
        'This is not Matrix']
print(List)
print(List[1][2])
print(List[3])

# Tuple
a = ('a', 19, 'hello')
print(type(a))
print(a)
a = 'a', 19, 'hello'
print(type(a))
print(a)

# Dictionary
a = {"math": 15, "history": 20, "Geography": 19, "chemistry": 19, "Physics": 19}
print(type(a))
print(a)
print(a["history"])

